class calculator {

  /**
  * Summe zwei parametere.
  *
  * @return integer summer a + b
  * @param integer $a nummer
  * @param integer $b nummer
  */
  public function summe($a, $b){
    $r = $a + $b;
    return $r;
  }

  /**
  * Rest zwei parametere.
  *
  * @return integer rest a + b
  * @param integer $a nummer
  * @param integer $b nummer
  */
  public function rest($a, $b){
    $r = $a + $b;
    return $r;
  }
}

